package com.neotech.lesson02;

public class VariableValuesHw2 {

	public static void main(String[] args) {

		byte varA = 9;
		short varB = 31355;
		int varC = 399258439;
		long varD = 5928832456l;
		float varE = 5.8967f;
		double varF = 6.92836758;
		char varG = 'g';
		boolean varH = true;

		System.out.println(varA);
		System.out.println(varB);
		System.out.println(varC);
		System.out.println(varD);
		System.out.println(varE);
		System.out.println(varF);
		System.out.println(varG);
		System.out.println(varH);

		System.out.println("You can find the re-assigned values below. ");
		// Re-assigning values of the variables
		varA = 11;
		varB = 3392;
		varC = 999432876;
		varD = 3182874893274l;
		varE = 9.2345f;
		varF = 3.12398745;
		varG = 'K';
		varH = false;
		System.out.println(varA);
		System.out.println(varB);
		System.out.println(varC);
		System.out.println(varD);
		System.out.println(varE);
		System.out.println(varF);
		System.out.println(varG);
		System.out.println(varH);

	}
}
